    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        // TODO add your handling code here:
        String nota1 = new String();
        String nota2 = new String();
        String nota3 = new String();
        String acum = new String();
        nota1 = txt_nota1.getText();
        nota2 = txt_nota2.getText();
        nota3 = txt_nota3.getText();
        acum = txt_acum.getText();
        float notauno  = Float.parseFloat(nota1);
        float notados = Float.parseFloat(nota2);
        float notatres = Float.parseFloat(nota3);
        float acumu = Float.parseFloat(acum);
        float total=0;
        total = notauno + notados + notatres + acumu;
        String totala  = Float.toString(total);
        txt_total.setText(totala);
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/siu", "root", "jorgito5828H");
            PreparedStatement pst = cn.prepareStatement("insert into asignacionnota values(?,?,?,?,?,?)");
            pst.setString(1, txt_nota1.getText().trim());
            pst.setString(2, txt_nota2.getText().trim());
            pst.setString(3, txt_nota3.getText().trim());
            pst.setString(4, txt_acum.getText().trim());
            pst.setString(5, txt_total.getText().trim());
            pst.setString(6, txt_buscar.getText().trim());
            pst.executeUpdate();

            txt_idalumno.setText("");
            txt_nombre.setText("");
            txt_curso.setText("");
            txt_nota1.setText("");
            txt_nota2.setText("");
            txt_nota3.setText("");
            txt_acum.setText("");
            JOptionPane.showMessageDialog(null, "Asignacion de nota exitosa");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Asignacion no registrada.");
        }
        
        
    }//GEN-LAST:event_jLabel7MouseClicked

